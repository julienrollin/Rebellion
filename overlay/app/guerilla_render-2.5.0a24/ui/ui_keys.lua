local GIZMO_REACH_DEFAULT_SCALE = 1.5
LUI_MODIFIER_SHIFT = 8
LUI_MODIFIER_CTRL = 16
LUI_MODIFIER_ALT = 32
LUIKeyDisableOnChar = nil
local function LUIHasModifier(modifier, flag)
  return modifier and modifier % (flag * 2) >= flag
end
local function LUISetGizmoPrecisionCtrl(c, pressed)
  if c ~= LUI_KEY_CONTROL and c ~= LUI_KEY_LCONTROL and c ~= LUI_KEY_RCONTROL then
    return
  end
  local keys = rawget(_G, "LUIGizmoPrecisionCtrlKeys")
  if not keys then
    keys = {}
    rawset(_G, "LUIGizmoPrecisionCtrlKeys", keys)
  end
  keys[c] = pressed and true or nil
  rawset(_G, "LUIGizmoPrecisionCtrlDown", (keys[LUI_KEY_CONTROL] or keys[LUI_KEY_LCONTROL] or keys[LUI_KEY_RCONTROL]) and true or false)
end
local function LUIGetRaySnapPlug()
  local globals = rawget(_G, "LUIGlobals")
  return globals and globals.RaySnap
end
local function LUIClearGizmoSnapState()
  rawset(_G, "LUIGizmoGridSnapDown", nil)
  rawset(_G, "LUIGizmoSurfaceSnapDown", nil)
  local plug = LUIGetRaySnapPlug()
  if rawget(_G, "LUIGizmoSurfaceSnapHeld") and plug then
    local previous = rawget(_G, "LUIGizmoSurfaceSnapPrev")
    LUIPlugSetValue(plug, previous and true or false)
  end
  rawset(_G, "LUIGizmoSurfaceSnapHeld", nil)
  rawset(_G, "LUIGizmoSurfaceSnapPrev", nil)
end
