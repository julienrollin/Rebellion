local function LUICanUseToolSizeShortcut()
  local focus = rawget(_G, "LUIFocusWindow")
  local target = focus and (focus.CharTarget or focus)
  return not (target and target.onChar)
end
local function LUIToggleTransformMode()
  local globals = rawget(_G, "LUIGlobals")
  local transformMode = globals and globals.TransformMode
  if not transformMode then
    return
  end
  local current = LUIPlugGetValue(transformMode) or "world"
  LUIPlugSetValue(transformMode, current == "local" and "world" or "local")
  return true
end
local function LUIGetGizmoReachScalePlug()
  local globals = rawget(_G, "LUIGlobals")
  if not globals then
    return
  end
  if not globals.GizmoReachScale then
    Plug(globals, "GizmoReachScale", Plug.NoSerial, LUIPSTypeFloat, GIZMO_REACH_DEFAULT_SCALE)
  end
  return globals.GizmoReachScale
end
local function LUIAdjustGizmoReachScale(delta)
  local plug = LUIGetGizmoReachScalePlug()
  if not plug then
    return
  end
  local value = LUIPlugGetValue(plug) or GIZMO_REACH_DEFAULT_SCALE
  if delta > 0 then
    value = value * 2
  else
    value = math.max(GIZMO_REACH_DEFAULT_SCALE, value / 2)
  end
  LUIPlugSetValue(plug, value)
  return true
end
local function LUIAdjustToolSize(delta)
  local globals = rawget(_G, "LUIGlobals")
  local transformTool = globals and globals.TransformTool and LUIPlugGetValue(globals.TransformTool)
  -- Custom move/scale/rotate gizmos share one reach scale so switching tools keeps the same adjusted size.
  if (transformTool == "move" or transformTool == "scale" or transformTool == "rotate") and LUIAdjustGizmoReachScale(delta) then
    return true
  end
  local act = delta > 0 and rawget(_G, "LUIIncreaseToolSizeAct") or rawget(_G, "LUIDecreaseToolSizeAct")
  if act then
    act()
    return true
  end
  local toolSize = globals and globals.ToolSize
  if toolSize then
    local value = LUIPlugGetValue(toolSize) or 10
    if delta > 0 then
      value = value + 2
    else
      value = math.max(4, value - 2)
    end
    LUIPlugSetValue(toolSize, value)
    return true
  end
end
local function LUIOnCharToolSizeShortcut(c, modifier)
  if LUIKeyDisableOnChar then
    return
  end
  if not LUICanUseToolSizeShortcut() then
    return
  end
  if LUIHasModifier(modifier, LUI_MODIFIER_CTRL) or LUIHasModifier(modifier, LUI_MODIFIER_ALT) then
    return
  end
  local ch
  if type(c) == "number" then
    if c < 0 or c > 255 then
      return
    end
    ch = string.char(c)
  else
    ch = c
  end
  if ch == "+" then
    return LUIAdjustToolSize(1)
  elseif ch == "-" then
    return LUIAdjustToolSize(-1)
  end
end
local function LUIOnKeyDownToolSizeShortcut(c, modifier)
  if not LUICanUseToolSizeShortcut() then
    return
  end
  if LUIHasModifier(modifier, LUI_MODIFIER_CTRL) or LUIHasModifier(modifier, LUI_MODIFIER_ALT) then
    return
  end
  local delta
  if c == LUI_KEY_ADD then
    delta = 1
  elseif c == LUI_KEY_SUBTRACT then
    delta = -1
  elseif c == LUI_KEY_OEM_PLUS then
    delta = 1
  elseif c == LUI_KEY_OEM_MINUS then
    delta = -1
  end
  if delta then
    LUIKeyDisableOnChar = true
    return LUIAdjustToolSize(delta)
  end
end
function LUIOnChar(c, modifier)
  if LUIColorPicker then
    LUIOnEndColorPicker()
    LUIUpdateDisplay()
    return
  end
  if LUIOnCharToolSizeShortcut(c, modifier) then
    LUIUpdateDisplay()
    return
  end
  if LUIFocusWindow and not LUIKeyDisableOnChar then
    local onChar = LUIFocusWindow.onChar
    if onChar then
      onChar(LUIFocusWindow.CharTarget or LUIFocusWindow, c, modifier)
    end
  end
  LUIUpdateDisplay()
end

local function LUIColorPickerIsCtrlAltShortcut(c, modifier)
  local ctrl = LUIHasModifier(modifier, LUI_MODIFIER_CTRL) or c == LUI_KEY_CONTROL or c == LUI_KEY_LCONTROL or c == LUI_KEY_RCONTROL
  local alt = LUIHasModifier(modifier, LUI_MODIFIER_ALT) or c == LUI_KEY_MENU
  return ctrl and alt
end

local function LUIColorPickerIsShortcut(c, modifier)
  return c == LUI_KEY_SHIFT or LUIColorPickerIsCtrlAltShortcut(c, modifier)
end

local function GRAutoSaveEnsureLoaded()
  local install = rawget(_G, "GRAutoSaveInstall")
  if not install then
    pcall(require, "ga_autosave")
    install = rawget(_G, "GRAutoSaveInstall")
  end
  if install then
    pcall(install)
  end
end

