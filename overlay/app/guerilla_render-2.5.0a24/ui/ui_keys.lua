function LUIOnKeyDown(c, modifier, _repeat)
  LUIKeyDisableOnChar = nil
  LUISetGizmoPrecisionCtrl(c, true)
  local autosaveTick = rawget(_G, "GRAutoSaveTick")
  if autosaveTick then
    pcall(autosaveTick, false)
  else
    GRAutoSaveEnsureLoaded()
  end
  if LUIIsolateLightTryKeyDown(c, modifier, _repeat) then
    LUIKeyDisableOnChar = true
    LUIUpdateDisplay()
    return
  end
  if LUIOnKeyDownToolSizeShortcut(c, modifier) then
    LUIUpdateDisplay()
    return
  end
  if c == LUI_KEY_INSERT and not _repeat and LUICanUseToolSizeShortcut() and LUIHasModifier(modifier, LUI_MODIFIER_CTRL) then
    local resetPivot = rawget(_G, "LUIResetPivotTransform")
    if resetPivot and resetPivot() then
      LUIUpdateDisplay()
      return
    end
  end
  if c == LUI_KEY_X and not _repeat and LUICanUseToolSizeShortcut() and not LUIHasModifier(modifier, LUI_MODIFIER_CTRL) and not LUIHasModifier(modifier, LUI_MODIFIER_ALT) then
    local togglePivotEdit = rawget(_G, "LUIToggleGizmoPivotEditMode")
    if togglePivotEdit and togglePivotEdit() then
      LUIUpdateDisplay()
      return
    end
  end
  if c == LUI_KEY_A and modifier == 0 and not _repeat and LUICanUseToolSizeShortcut() and LUIToggleTransformMode() then
    LUIUpdateDisplay()
    return
  end
  if not LUIColorPicker and LUIColorPickerIsShortcut(c, modifier) and not _repeat then
    local picker = rawget(_G, "LUIColorPickerWindow")
    if picker and not LUIWindowIsDestroyed(picker) and picker.onStartPickColor then
      if LUIColorPickerIsCtrlAltShortcut(c, modifier) then
        picker:onStartPickColor(picker._PickButton, false, false, true)
      else
        picker:onStartPickColor(picker._PickButton, false, true)
      end
      LUIUpdateDisplay()
      return
    end
  end
  if LUIColorPicker and LUIColorPickerIsShortcut(c, modifier) then
    LUIUpdateDisplay()
    return
  end
  if LUIColorPicker then
    LUIOnEndColorPicker()
    LUIUpdateDisplay()
    return
  end
  local key = LUIGetKey(_G, c)
  if LUIMouseKeyDisplay then
    LUIMouseKeyDisplaySetKey(LUIMouseKeyDisplay, c, "down")
  end
  if LUIFocusWindow then
    local win = LUIFocusWindow
    while win do
      local onKeyDown = win.onKeyDown
      if onKeyDown then
        local target = win.KeyTarget or win
        if not LUIWindowIsDestroyed(target) and onKeyDown(target, c, modifier, _repeat) then
          break
        end
      end
      win = win._Parent
    end
  end
  LUIUpdateDisplay()
end
function LUIOnKeyUp(c, modifier)
  LUISetGizmoPrecisionCtrl(c, false)
  if LUIColorPicker and (c == LUI_KEY_SHIFT or c == LUI_KEY_CONTROL or c == LUI_KEY_LCONTROL or c == LUI_KEY_RCONTROL or c == LUI_KEY_MENU) then
    LUIOnEndColorPicker()
    LUIUpdateDisplay()
    return
  end
  if LUIColorPicker then
    LUIOnEndColorPicker()
    LUIUpdateDisplay()
    return
  end
  if LUIMouseKeyDisplay then
    LUIMouseKeyDisplaySetKey(LUIMouseKeyDisplay, c, "up")
  end
  if LUIFocusWindow then
    local win = LUIFocusWindow
    while win do
      local onKeyUp = win.onKeyUp
      if onKeyUp then
        if onKeyUp(win.KeyTarget or win, c, modifier) then
          break
        end
      end
      win = win._Parent
    end
  end
  LUIUpdateDisplay()
end
